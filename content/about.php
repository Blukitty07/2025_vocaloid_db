<p>This database aims to be a small apprication place in which people can discover vocaloid, utauloid and synthV songs and be able to easily discover information about these said songs</p>
<h3>What are Vocaloids?</h3>
<p>Vocaloids are a voice synthesizer computer softwares that are developed by Yamaha, and are used as instruments in songs. The vocaloid software uses voicebanks to generate vocal sounds (which is done by hand with absolutely no AI involved). The vocaloid voicebank are created by having a voice provider record a bank of voice samples, which is what the engine uses to construct songs. To be able to use vocaloids you must purchase the software (like you would any other instrument) to be able to use it. The first vocaloids were made in spain in 2000, but it wasn’t until Hatsune Miku was created in 2007 by YAMAHA that the popularity of vocaloids surged.</p>
<h3>What is the difference between Vocaloids, Utauloids and SynthV?</h3>
<p>When people talk about vocaloids, often the topic of utauloids and synthv come up. UTAU is a japanese program made in 2008, with the program itself and most voicebanks made by it being completely free. You can also make your own voicebanks in UTAU, and they are able to sing in basically any language, while delivering similar results to a vocaloid.</p><p> SynthV much like vocaloid will use create soundbanks using a voice provider to build the soundbank but they also add in AI in order to give it a more “lifelike” sound. They are also cheaper to purchase than vocaloids and have some free versions of SynthV soundbanks.</p>

